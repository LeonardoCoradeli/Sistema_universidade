    private void btn_mostrar_departamentosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mostrar_departamentosActionPerformed
        Departamento[] dep=Controlador.universidade.exibir_departamento();
        if(model.getRowCount()!=0){
            for(int i=0;i<dep.length;i++){
                model.removeRow(0);
            }
            for(int i=0;i<dep.length;i++){
                if(dep[i]!=null){
                  Object[] valores={dep[i].exibir_cod(),dep[i].exibir_nome(),dep[i].exibir_gastos()};
                  model.addRow(valores);  
                }
            }
        }
        else{
            for(int i=0;i<dep.length;i++){
                if(dep[i]!=null){
                  Object[] valores={dep[i].exibir_cod(),dep[i].exibir_nome(),dep[i].exibir_gastos()};
                  model.addRow(valores);    
                }
            }
        } 
    }//GEN-LAST:event_btn_mostrar_departamentosActionPerformed

    private void buscar_departamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscar_departamentoActionPerformed
        if(buscar_valor.getText().equals("")){
            preencher_valor.setVisible(true);
        }
        else{
            Departamento[] dep=Controlador.universidade.buscar_departamento_valor(Double.parseDouble(buscar_valor.getText()));
            if(model.getRowCount()!=0){
                for(int i=0;i<dep.length;i++){
                    model.removeRow(0);
                }
                for(int i=0;i<dep.length;i++){
                    if(dep[i]!=null){
                      Object[] valores={dep[i].exibir_cod(),dep[i].exibir_nome(),dep[i].exibir_gastos()};
                      model.addRow(valores);  
                    }
                }
            }
            else{
                for(int i=0;i<dep.length;i++){
                    if(dep[i]!=null){
                      Object[] valores={dep[i].exibir_cod(),dep[i].exibir_nome(),dep[i].exibir_gastos()};
                      model.addRow(valores);    
                    }
                }
            } 
                preencher_valor.setVisible(false);
        }
        
    }//GEN-LAST:event_buscar_departamentoActionPerformed

